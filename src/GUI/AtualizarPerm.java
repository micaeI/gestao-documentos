    private void jComboBoxPermIdItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBoxPermIdItemStateChanged

        posiPerm = String.valueOf(jComboBoxPermId.getSelectedItem());
        PermissoesDocDAO PDDAO = new PermissoesDocDAO();
        permissao = PDDAO.listar();
        for (int i = 0; i < permissao.size(); i++) {
            if (permissao.get(i).getPerm_id() == (Integer.parseInt(posiPerm))) {
                U = permissao.get(i);
            }
        }

    }//GEN-LAST:event_jComboBoxPermIdItemStateChanged

    private void jButtonBuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonBuscarMouseClicked

        try {
            DocDAO ddDAO = new DocDAO();
            UsersDAO uuDAO = new UsersDAO();
            D = ddDAO.procurar(U.getDoc_id());
            jComboBoxDoc.setSelectedItem(D.getTitulo());
            Us = uuDAO.procurar(U.getUser_id());
            jComboBoxUsuario.setSelectedItem(Us.getNome());
        } catch (Exception e) {
            System.out.println("Falha na localização");
        }
        jComboBoxPerm.setSelectedItem(U.getPermission_type());
    }//GEN-LAST:event_jButtonBuscarMouseClicked

