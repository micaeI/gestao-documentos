    public static void carregaComboDoc() {
        jComboBoxDoc.removeAllItems();
        DocDAO dDAO = new DocDAO();
        documento = dDAO.listar();

        for (int i = 0; i < documento.size(); i++) {
            jComboBoxDoc.addItem(documento.get(i).getTitulo());

        }
    }

    public static void carregaComboUser() {
        jComboBoxUsuario.removeAllItems();
        UsersDAO dDAO = new UsersDAO();
        user = dDAO.listar();

        for (int i = 0; i < user.size(); i++) {
            jComboBoxUsuario.addItem(user.get(i).getNome());

        }
    }

    public static void carregaComboPerm() {
        jComboBoxPermId.removeAllItems();
        PermissoesDocDAO dDAO = new PermissoesDocDAO();
        permissao = dDAO.listar();

        for (int i = 0; i < permissao.size(); i++) {
            jComboBoxPermId.addItem(String.valueOf(permissao.get(i).getPerm_id()));

        }
    }

