   private void userchanged() {
        posiUser = String.valueOf(jComboBoxUsuario.getSelectedItem());
        UsersDAO UDAO = new UsersDAO();
        user = UDAO.listar();

        for (int i = 0; i < user.size(); i++) {
            if (user.get(i).getNome().equals(posiUser)) {
                Us = user.get(i);
                idUser = Us.getUser_id();  
                break;
            }
        }

    }

    private void docchanged() {
        posiDoc = String.valueOf(jComboBoxDoc.getSelectedItem());
        DocDAO dDAO = new DocDAO();
        documento = dDAO.listar();
        for (int i = 0; i < documento.size(); i++) {
            if (documento.get(i).getTitulo().equals(posiDoc)) {
               idDoc = documento.get(i).getDoc_id();
               break;
            }
        }

    }
